    private void tambahProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahProductActionPerformed
        // TODO add your handling code here:
        try {
            kelas.product prd = new kelas.product(); 
            prd.setProductName(nameProduct.getText()); 
            prd.setProductDescription(descriptionProduct.getText()); 
            prd.setProductPrice(Integer.parseInt(priceProduct.getText())); 
            
            if (comboBoxCategoryProduct.getSelectedItem().equals("Active")) { 
                prd.setProductCategory(1); 
            } else { 
                prd.setProductCategory(0); 
            } 
            
            prd.TambahProduct(); 
            JOptionPane.showMessageDialog(this, "Data Berhasil Ditambah");
            load_table(); 
            reset();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Tambah: " + e.getMessage());
        }
    }//GEN-LAST:event_tambahProductActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
    int row = jTable1.getSelectedRow(); 
        if (row != -1){ 
            idProduct.setText(jTable1.getValueAt(row, 0).toString()); 
            nameProduct.setText(jTable1.getValueAt(row, 1).toString()); 
            String category = jTable1.getValueAt(row, 2).toString(); 
            descriptionProduct.setText(jTable1.getValueAt(row, 3).toString()); 
            priceProduct.setText(jTable1.getValueAt(row, 4).toString()); 
            
            if (category.equals("Active")){ 
                comboBoxCategoryProduct.setSelectedItem("Active"); 
            } else { 
                comboBoxCategoryProduct.setSelectedItem("Inactive"); 
            } 
        }
    }//GEN-LAST:event_jTable1MouseClicked

