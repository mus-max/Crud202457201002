    private void hapusProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusProductActionPerformed
        // TODO add your handling code here:
        try {
            int confirm = JOptionPane.showConfirmDialog(this, "Yakin hapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                kelas.product prd = new kelas.product(); 
                prd.setProductId(Integer.parseInt(idProduct.getText())); 
                prd.HapusProduct(); 
                load_table(); 
                reset(); 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
        }         
    }//GEN-LAST:event_hapusProductActionPerformed

