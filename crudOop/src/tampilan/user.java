    private void ubahUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahUserActionPerformed
        // TODO add your handling code here:
        try {
            User usr = new User(); 
            usr.setUserName(fieldUsenameUser.getText()); 
            usr.setUserEmail(fieldEmailUser.getText()); 
            usr.setUserPassword(fieldPasswodUser.getText()); 
            usr.setUserFullName(fieldFullnameUser.getText()); 
            
            if (comboBoxStatusUser.getSelectedItem().equals("Active")) { 
                usr.setUserStatus(1); 
            } else { 
                usr.setUserStatus(0); 
            } 
            usr.UbahUser(); 
            JOptionPane.showMessageDialog(this, "User Berhasil Diubah");
            load_table(); 
            reset();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Ubah: " + e.getMessage());
        }
    }//GEN-LAST:event_ubahUserActionPerformed

