    private void hapusUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusUserActionPerformed
        // TODO add your handling code here:
        try {
            int confirm = JOptionPane.showConfirmDialog(this, "Hapus user ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                kelas.User usr = new kelas.User(); 
                usr.setUserName(fieldUsenameUser.getText()); 
                usr.HapusUser(); 
                load_table(); 
                reset();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
        }
    }//GEN-LAST:event_hapusUserActionPerformed

    private void TblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblUserMouseClicked
        // TODO add your handling code here:
        int row = TblUser.getSelectedRow(); 
        if (row != -1){ 
            fieldUsenameUser.setText(TblUser.getValueAt(row, 0).toString()); 
            fieldEmailUser.setText(TblUser.getValueAt(row, 1).toString()); 
            fieldFullnameUser.setText(TblUser.getValueAt(row, 2).toString()); 
            String Status = TblUser.getValueAt(row, 3).toString(); 
             
            if (Status.equalsIgnoreCase("Active")){ 
                comboBoxStatusUser.setSelectedItem("Active"); 
            } else { 
                comboBoxStatusUser.setSelectedItem("Inactive"); 
            } 
            fieldUsenameUser.setEditable(false);
        }
    }//GEN-LAST:event_TblUserMouseClicked

